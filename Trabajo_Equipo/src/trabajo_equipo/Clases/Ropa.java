package trabajo_equipo.Clases;

public class Ropa {
   String talla;
   String genero;

    public Ropa() {
    }

    public Ropa(String talla, String genero) {
        this.talla = talla;
        this.genero = genero;
    }

    public String getTalla() {
        return talla;
    }

    public void setTalla(String talla) {
        this.talla = talla;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }
   
   
   
   
   
}
